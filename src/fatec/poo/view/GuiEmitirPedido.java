    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtQtdVendida.getText().equals("0")){
            JOptionPane.showMessageDialog(this, "Quantidade invÃ¡lida", "Erro quantidade", 2);
            txtQtdVendida.requestFocus();
            return;
        }   
            
        if(Integer.parseInt(txtQtdVendida.getText()) > cadProduto.get(posProduto).getQtdeEstoque()){
            JOptionPane.showMessageDialog(this, "Estoque insuficiente", "Erro quantidade", 2);
            txtQtdVendida.requestFocus();
            return;
        }
        
        if(Integer.parseInt(txtQtdVendida.getText()) > cadProduto.get(posProduto).getQtdeEstoque() - cadProduto.get(posProduto).getEstoqueMinimo()){
            JOptionPane.showMessageDialog(this, "Estoque insuficiente", "Erro quantidade", 2);
            txtQtdVendida.requestFocus();
            return;
        }
        
        double total = cadProduto.get(posProduto).getPreco() * Integer.parseInt(txtQtdVendida.getText());
        int quantidade = Integer.parseInt(txtQtdVendida.getText());
        
        if(total > ((Cliente)cadCliVend.get(posCliente)).getLimiteDisp()){
            JOptionPane.showMessageDialog(this, "Saldo insuficiente", "Erro adiÃ§Ã£o de item", 2);
            txtQtdVendida.requestFocus();
            return;
        }
        
        ((Cliente)cadCliVend.get(posCliente)).setLimiteDisp(
            ((Cliente)cadCliVend.get(posCliente)).getLimiteDisp()
            - total
        );
        
        cadProduto.get(posProduto).setQtdeEstoque(
            cadProduto.get(posProduto).getQtdeEstoque()
            - Integer.parseInt(txtQtdVendida.getText())
        );
        
        String linha [] = {
            String.valueOf(cadProduto.get(posProduto).getCodigo()),
            String.valueOf(cadProduto.get(posProduto).getDescricao()),
            String.valueOf(cadProduto.get(posProduto).getPreco()),
            txtQtdVendida.getText(),
            String.valueOf(total)
        };
        
        modTblPedido.addRow(linha);
        totalPedido += total;
        qtdPedido += quantidade;
        txtValorTotalPedido.setText(String.valueOf(totalPedido));
        txtQtdItens.setText(String.valueOf(qtdPedido));
        
        ItemPedido item = new ItemPedido(
            modTblPedido.getRowCount() - 1,
            Integer.parseInt(txtQtdVendida.getText()),
            cadProduto.get(posProduto)                
        );
        
        
        
        cadItens.add(item);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int rowToBeRemoved = tblPedidos.getSelectedRow();
        int posProduto;
        int posItem;
        if(rowToBeRemoved < 0)
            JOptionPane.showMessageDialog(this, "Item nÃ£o selecionado", "Erro de remoÃ§Ã£o", 2);
        else {
            for(posItem = 0; posItem < cadItens.size(); posItem++){
                if(cadItens.get(posItem).getSequencia() == rowToBeRemoved)
                    break;
            }
            
            double qtdVendida = cadItens.get(posItem).getQtdeVendida();
            
            double subtotal =
                cadItens.get(posItem).getProduto().getPreco()
                * cadItens.get(posItem).getQtdeVendida();
         
            cadItens.get(posItem).getProduto().setQtdeEstoque(
                cadItens.get(posItem).getProduto().getQtdeEstoque()
                + qtdVendida
            );
            
            ((Cliente)cadCliVend.get(posCliente)).setLimiteDisp(
                ((Cliente)cadCliVend.get(posCliente)).getLimiteDisp()
                + subtotal
            );
            
            modTblPedido.removeRow(rowToBeRemoved);
            
            totalPedido -= subtotal;
            qtdPedido -= qtdVendida;
            
            txtValorTotalPedido.setText(String.valueOf(subtotal));
            txtQtdItens.setText(String.valueOf(qtdPedido));
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

