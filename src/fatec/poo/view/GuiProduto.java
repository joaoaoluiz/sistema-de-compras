    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        Produto produto = new Produto(txtCod.getText(), txtDescricao.getText());
        produto.setEstoqueMinimo(Double.parseDouble(txtEstoqueMin.getText()));
        produto.setPreco(Double.parseDouble(txtPrecoUnitario.getText()));
        produto.setQtdeEstoque(Integer.parseInt(txtQtdDisponivel.getText()));
        
        cadastro.add(produto);
        
        prepararNovaEntrada();
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        txtCod.setEnabled(false);
        
        cadastro.get(posProduto).setEstoqueMinimo(Double.parseDouble(txtEstoqueMin.getText()));
        cadastro.get(posProduto).setPreco(Double.parseDouble(txtPrecoUnitario.getText()));
        cadastro.get(posProduto).setQtdeEstoque(Double.parseDouble(txtQtdDisponivel.getText()));
        cadastro.get(posProduto).setPreco(Double.parseDouble(txtPrecoUnitario.getText()));
        cadastro.get(posProduto).setDescricao(txtDescricao.getText());
        
        prepararNovaEntrada();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    int dialog = JOptionPane.showConfirmDialog(null, "Excluir registro?", "Confirmar", 0);
    
    if(dialog == 0){
        cadastro.remove(posProduto);
        prepararNovaEntrada();
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

