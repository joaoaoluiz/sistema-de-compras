    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try{
            Integer.parseInt(txtCod.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Código inválido");
            txtCod.setText(null);
            txtCod.requestFocus();
            return;
        }
        
        int x;
        for (x = 0; x < cadastro.size(); x++) {
            if (cadastro.get(x).getCodigo().equals(txtCod.getText())) {
                break;
            }
        }

        if (x < cadastro.size()) {
            posProduto = x;
        } else {
            posProduto = -1;
        }
        
        if(posProduto >= 0){
            txtDescricao.setText(cadastro.get(x).getDescricao());
            txtEstoqueMin.setText(String.valueOf(cadastro.get(x).getEstoqueMinimo()));
            txtPrecoUnitario.setText(String.valueOf(cadastro.get(x).getPreco()));
            txtQtdDisponivel.setText(String.valueOf(cadastro.get(x).getQtdeEstoque()));
            
            btnConsultar.setEnabled(false);
            btnIncluir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
        } else {
            btnConsultar.setEnabled(false);
            btnIncluir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtDescricao.requestFocus();
        }
        txtCod.setEnabled(false);
        txtDescricao.setEnabled(true);
        txtEstoqueMin.setEnabled(true);
        txtPrecoUnitario.setEnabled(true);
        txtQtdDisponivel.setEnabled(true);
    }//GEN-LAST:event_btnConsultarActionPerformed

